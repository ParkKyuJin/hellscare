package com.pro.hellscare.VO;

public class NewsVO {
	private String newsTitle;
	private String newsThumbnail;
	private String newsContents;
	private String newsSummary;
	private String newsDate;
	private String newsAuthor;
	private String newsUrl;
	private String newsImg;
	private String newsCaption;
	
	public NewsVO() {}
	
	public String getNewsTitle() {
		return newsTitle;
	}
	
	public void setNewsTitle(String newsTitle) {
		this.newsTitle = newsTitle;
	}
	
	public String getNewsThumbnail() {
		return newsThumbnail;
	}
	
	public void setNewsThumbnail(String newsThumbnail) {
		this.newsThumbnail = newsThumbnail;
	}
	
	public String getNewsContents() {
		return newsContents;
	}
	
	public void setNewsContents(String newsContents) {
		this.newsContents = newsContents;
	}

	public String getNewsSummary() {
		return newsSummary;
	}

	public void setNewsSummary(String newsSummary) {
		this.newsSummary = newsSummary;
	}

	public String getNewsDate() {
		return newsDate;
	}

	public void setNewsDate(String newsDate) {
		this.newsDate = newsDate;
	}

	public String getNewsAuthor() {
		return newsAuthor;
	}

	public void setNewsAuthor(String newsAuthor) {
		this.newsAuthor = newsAuthor;
	}
	
	public String getNewsUrl() {
		return newsUrl;
	}
	
	public void setNewsUrl(String newsUrl) {
		this.newsUrl = newsUrl;
	}
	
	public String getNewsImg() {
		return newsImg;
	}
	
	public void setNewsImg(String newsImg) {
		this.newsImg = newsImg;
	}
	
	public String getNewsCaption() {
		return newsCaption;
	}
	
	public void setNewsCaption(String newsCaption) {
		this.newsCaption = newsCaption;
	}
}
